/* CMT BUTTON ICON with LED */
void drawinfo_cmt_icon(int cmd,BYTE lampon)
{
	static BYTE cmt_ctbl[5] =
	{ INFO_BLACK,INFO_BLACK,INFO_GRAY,INFO_HALF_GREEN ,INFO_GREEN };

	cmt_ctbl[4] = lampon ? INFO_GREEN : INFO_GRAY;
	switch(cmd)
	{
	case 0:
		drawinfo_icon(stop_icon,TOOL_X_STOP_ICON,0,16,15,cmt_ctbl);
		break;
	case 2:
		drawinfo_icon(play_icon,TOOL_X_LOAD_ICON,0,32,15,cmt_ctbl);
		break;
	case 3:
	case 5:
		drawinfo_icon(ff_icon,TOOL_X_FF_ICON,0,16,15,cmt_ctbl);
		/* reset APSS flash counter */
		apss_fcount = GetTickCount();
		apss_lamp = 1;
		break;
	case 4:
	case 6:
		drawinfo_icon(rew_icon,TOOL_X_REW_ICON,0,16,15,cmt_ctbl);
		/* reset APSS flash counter */
		apss_fcount = GetTickCount();
		apss_lamp = 1;
		break;
	case 10:
		cmt_ctbl[4] = lampon ? INFO_RED : INFO_GRAY;
		drawinfo_icon(rec_icon,TOOL_X_REC_ICON,0,16,15,cmt_ctbl);
		break;
	}
}

void drawinfo_cmt_cnt(void)
{
	DWORD counter;
	char buf[10];

	if( tape.header.frequency != 0)
	{
		counter = tape.header.position *10 / tape.header.frequency;
		if( cmtcnt != counter)
		{
			cmtcnt = counter;
			counter /= 10;
			sprintf(buf,"%02d:%02d",(int)(counter/60),(int)(counter%60));
			drawinfo_string(buf,5,TOOL_X_CMT_COUNT,0,INFO_WHITE);
		}
		/* APSS flash counter */
		if( (tape.cur_cmd == 5 || tape.cur_cmd == 6) && (GetTickCount()-apss_fcount)>APSS_FLASH_TICK)
		{
			apss_fcount += APSS_FLASH_TICK;
			apss_lamp ^= 1;
			drawinfo_cmt_icon(tape.cur_cmd,apss_lamp);
		}
	}
	else
	if( cmtcnt != 0xffffffff )
	{
		cmtcnt = 0xffffffff;
		drawinfo_string("",5,TOOL_X_CMT_COUNT,0,INFO_WHITE);
	}
}

