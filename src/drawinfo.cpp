/* mark dirty */
static void mark_dirty(void)
{
	inforenewal = scrnflash = 1;
}


/* user X1 font */
static void drawinfo_string(char *message,int nums,int sx,int sy,int color)
{
	DWORD y,x;
	BYTE chr;
	BYTE *src,*dst;
	BYTE fixnum = nums;

	dst = infoscreenmap + sy*SCREEN_WIDTH + sx;
	while((chr=*message) || nums)
	{
		if( chr != 0x00 )
			message++;
		else
			chr = 0x20;
		src = &KNJ_FNT[chr*16];
		for( y = 0 ;y < 16 ; y++)
		{
			chr = *src++;
			for( x = 0x80 ; x ; x>>=1 )
			{
				*dst++ = (chr & x) ? color : INFO_BACK;
			}
			dst += SCREEN_WIDTH-8;
		}
		dst -= 16*SCREEN_WIDTH-8;
		if( nums )
			if(--nums ==0)
				break;
	}
	mark_dirty();
}

/* 4bpp pattern & color table */
static void drawinfo_icon(const BYTE *src,int sx,int sy,int dx,int dy,const BYTE *ctable)
{
	BYTE *dst;
	BYTE y,x,col;

	dst = infoscreenmap + sy*SCREEN_WIDTH + sx;
	for( y = 0 ;y < dy ; y++)
	{
		for( x = 0 ; x<dx ; x+=2 )
		{
			col = *src++;
			if((col&0xf0)!=0xf0)
				dst[0] = ctable[col>>4];
			if((col&=0x0f)!=0x0f)
				dst[1] = ctable[col];
			dst+= 2;
		}
		dst += SCREEN_WIDTH-dx;
	}
	mark_dirty();
}

